    @push('scripts')
        <script src="https://cdn.jsdelivr.net/npm/chart.js"></script>
        <script>


            fetch('http://127.0.0.1:8002/api/report/companiesHot') // Giả sử API trả về số lượng bài viết của các công ty
                .then(response => response.json())
                .then(responseData => {
                    const companyNames = responseData.data.map(item => item.company_name); // Tên công ty
                    const newsCounts = responseData.data.map(item => item.number_of_news); // Số lượng bài viết

                    new Chart(document.getElementById('companyBarChart'), {
                        type: 'bar',
                        data: {
                            labels: companyNames,
                            datasets: [{
                                label: 'Số lượng bài viết',
                                data: newsCounts,
                                backgroundColor: 'rgba(54, 162, 235, 0.2)',
                                borderColor: 'rgba(54, 162, 235, 1)',
                                borderWidth: 1
                            }]
                        },
                        options: {
                            responsive: true,
                            scales: {
                                y: {
                                    beginAtZero: true
                                }
                            }
                        }
                    });
                });

            fetch('http://127.0.0.1:8002/api/report/showNewsHot') // Giả sử API trả về danh sách bài viết hot
                .then(response => response.json())
                .then(responseData => {
                    const titles = responseData.data.map(item => item.title); // Tiêu đề tin tức
                    const commentCounts = responseData.data.map(item => item.comments_count); // Số lượng bình luận
                    new Chart(document.getElementById('popularNewsChart'), {
                        type: 'bar',
                        data: {
                            labels: titles,
                            datasets: [{
                                label: 'Số lượng bình luận',
                                data: commentCounts,
                                backgroundColor: 'rgba(54, 162, 235, 0.2)',
                                borderColor: 'rgba(54, 162, 235, 1)',
                                borderWidth: 1
                            }]
                        },
                        options: {
                            responsive: true,
                            scales: {
                                y: {
                                    beginAtZero: true
                                }
                            }
                        }
                    });
                });

            fetch('http://127.0.0.1:8002/api/report/countUser')
                .then(response => response.json())
                .then(data => {
                    if (data.status === 'success' && Array.isArray(data.data) && data.data.length > 0) {
                        const customerLabels = data.data.map(item => item.month);
                        const customerCounts = data.data.map(item => item.total);
                        new Chart(document.getElementById('usersPieChart'), {
                            type: 'pie',
                            data: {
                                labels: customerLabels,
                                datasets: [{
                                    label: 'Users Distribution',
                                    data: customerCounts,
                                    backgroundColor: [
                                        'rgba(255, 99, 132, 0.2)',
                                        'rgba(54, 162, 235, 0.2)',
                                        'rgba(255, 206, 86, 0.2)',
                                        'rgba(75, 192, 192, 0.2)',
                                        'rgba(153, 102, 255, 0.2)',
                                        'rgba(255, 159, 64, 0.2)'
                                    ],
                                    borderColor: [
                                        'rgba(255, 99, 132, 1)',
                                        'rgba(54, 162, 235, 1)',
                                        'rgba(255, 206, 86, 1)',
                                        'rgba(75, 192, 192, 1)',
                                        'rgba(153, 102, 255, 1)',
                                        'rgba(255, 159, 64, 1)'
                                    ],
                                    borderWidth: 1
                                }]
                            },
                            options: {
                                responsive: true,
                                plugins: {
                                    legend: {
                                        position: 'top'
                                    },
                                    tooltip: {
                                        callbacks: {
                                            label: function(context) {
                                                const total = context.dataset.data.reduce((a, b) => a + b, 0);
                                                const value = context.raw;
                                                const percentage = ((value / total) * 100).toFixed(2);
                                                return `${context.label}: ${value} (${percentage}%)`;
                                            }
                                        }
                                    }
                                }
                            }
                        });
                    } else {
                        console.error('No data received from API');
                    }
                })
                .catch(error => console.error('Error fetching data:', error));
        </script>
